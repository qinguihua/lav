 * @property \Illuminate\Support\Carbon|null $updated_at
 * @property-read \App\Models\Classify $classify
 * @method static \Illuminate\Database\Eloquent\Builder|\App\Models\Article whereAuthor($value)
 * @method static \Illuminate\Database\Eloquent\Builder|\App\Models\Article whereClassifyId($value)
